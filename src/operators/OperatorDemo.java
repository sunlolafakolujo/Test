package operators;

public class OperatorDemo {
    public static void main(String[] args) {
        char ch="134".charAt(1);
        System.out.println(ch);

        ch++;
        System.out.println(ch);

        char cha='A';
        System.out.println(cha++);

        char ch1='B';
        System.out.println(++ch1);
    }
}
